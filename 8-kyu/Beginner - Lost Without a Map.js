// Given an array of integers, return a new array with each value doubled.

// For example:

// [1, 2, 3] --> [2, 4, 6]

function maps(x){
    //P: take in an array of integers (x)
    //R: a new array with each value doubled
    //E:
    //P: 
    let doubled = x.map(x => x*2)
    return doubled    
}